"""
Integration tests for speech queue priority behaviour.

Two scenarios tested end-to-end through the real TextToSpeechRunner /
JobQueue / Voice stack (TTS provider and sox stubbed out):

1. Signoff / scheduled shutdown
   Voice.clear_queue() must empty any pre-existing pending jobs so the
   shutdown announcement becomes the first item in an otherwise empty queue,
   not the last item behind whatever the DJ was already preparing to say.

2. Extension announcement ordering
   ExtensionManager calls voice.prepare_to_say(text, save_for_last=True)
   from a background thread.  The announcement must land *after* every chunk
   already being generated by a concurrent speak() call.  Without the
   _active_generations counter this was a race: if the extension thread saw
   an empty queue it would proceed immediately and get sandwiched before the
   chunks that were about to be added.
"""
import threading
import time
from pathlib import Path
from unittest.mock import MagicMock

import pytest

from tts.providers import BaseTTSProvider, TTSProviderType
from tts.tts_runner import TTSConfig, TextToSpeechRunner


# ---------------------------------------------------------------------------
# Shared stub / factory helpers
# ---------------------------------------------------------------------------

class _StubProvider(BaseTTSProvider):
    """Stub provider: creates an empty file without doing any real synthesis."""

    def __init__(self, generation_delay: float = 0.0):
        self._delay = generation_delay

    def load(self) -> None:
        pass

    def generate_speech_file(self, text: str, output_path: str, **kwargs) -> None:
        if self._delay:
            time.sleep(self._delay)
        Path(output_path).touch()

    def supports_voice_cloning(self) -> bool:
        return False

    def supported_languages(self):
        return []

    def is_available(self) -> bool:
        return True

    def metadata_info(self):
        return {}


def _make_runner(tmp_path, monkeypatch, generation_delay: float = 0.0) -> TextToSpeechRunner:
    """Return a TextToSpeechRunner backed by the stub provider.

    combine_audio_files is replaced with a no-op so sox is never invoked.
    """
    provider = _StubProvider(generation_delay=generation_delay)
    monkeypatch.setattr("tts.tts_runner.get_provider", lambda config: provider)

    config = TTSConfig(
        model=None,
        provider=TTSProviderType.COQUI,
        filepath=str(tmp_path / "tts_test"),
        auto_play=False,
        delete_interim_files=False,
        overwrite=True,
    )
    runner = TextToSpeechRunner(config)
    runner.combine_audio_files = MagicMock(return_value=None)
    return runner


